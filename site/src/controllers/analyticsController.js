var analyticsModel = require("../models/analyticsModel");

var sessoes = [];

function curtidos(req, res) {

    analyticsModel.curtidos()
        .then(
            function (resultado) {
                res.json(resultado);
            }
        )
        .catch(
            function (erro) {
                console.log(erro);
                console.log("Houve um erro ao realizar o post: ", erro.sqlMessage);
                res.status(500).json(erro.sqlMessage);
            }
        );

}

